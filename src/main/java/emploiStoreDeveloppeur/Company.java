package emploiStoreDeveloppeur;
public class Company {

	private String siret;
	private String name;
	
	public Company() {

	}

	public String getSiret() {
		return siret;
	}

	public void setSiret(String siret) {
		this.siret = siret;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
